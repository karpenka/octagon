function geks(a,w,h,L)
%a - side of geks, w - width, h - height, L - length
x0=['OFF'];
x1=[12 8 0;...
w/2 0 0;...
w/2 h 0;...
-w/2 h 0;...
-w/2 0 0;...
a/2 h/2-a*(1+sqrt(2))/2 L;...
a*(1+sqrt(2))/2 h/2-a/2 L;...
a*(1+sqrt(2))/2 h/2+a/2 L;...
a/2 h/2+a*(1+sqrt(2))/2 L;...
-a/2 h/2-a*(1+sqrt(2))/2 L;...
-a*(1+sqrt(2))/2 h/2-a/2 L;...
-a*(1+sqrt(2))/2 h/2+a/2 L;...
-a/2 h/2+a*(1+sqrt(2))/2 L];
x2=[4 2 1 7 11;...
4 6 5 0 1;...
4 3 0 4 8;...
4 9 10 2 3];
x3=[3 7 6 1;...
3 4 5 0;...
3 9 8 3;...
3 10 11 2];
dlmwrite('geks.off',x0,'')
dlmwrite('geks.off',x1,'-append','delimiter',' ')
dlmwrite('geks.off',x2,'-append','delimiter',' ')
dlmwrite('geks.off',x3,'-append','delimiter',' ')
end
